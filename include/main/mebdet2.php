Мягкая мебель предназначена для сидения и лежания. Бывает помягче, бывает пожёстче. Обитая тканью,
обитая кожей, отделанная резными деревянными вставками, с кисточками, с декоративными подушками,
раскладывающаяся в кровать. Для гостей, для себя. В наборе и по отдельности. Угловая, одно-, двух- и
трёхместная. Компактная или напротив, широкая.